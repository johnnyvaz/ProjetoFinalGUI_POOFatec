private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    // cria um objeto da classe Disciplina
    Disciplina disciplina = new Disciplina();
    disciplina.setNome(nome.getText());
    disciplina.setCargaHoraria(cargaHoraria.getText());
    disciplina.setProfessor(professor.getText());
        
    // view acessa o controller e recebe o resultado
    DisciplinaController disciplinaController = new DisciplinaController();
    if (disciplinaController.insere(disciplina) == 1){
        JOptionPane.showMessageDialog(null, 
                "Inserção com sucesso");
    }
    else {
        JOptionPane.showMessageDialog(null, 
                "Problema na Inserção");
    }
}//GEN-LAST:event_jButton1ActionPerformed

